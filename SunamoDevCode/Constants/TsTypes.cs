namespace SunamoDevCode.Constants;

/// <summary>
/// TypeScript primitive and built-in type names as string constants.
/// </summary>
public class TsTypes
{
    /// <summary>
    /// TypeScript string type.
    /// </summary>
    public const string String = "string";

    /// <summary>
    /// TypeScript number type.
    /// </summary>
    public const string Number = "number";

    /// <summary>
    /// TypeScript bigint type.
    /// </summary>
    public const string Bigint = "bigint";

    /// <summary>
    /// TypeScript boolean type.
    /// </summary>
    public const string Boolean = "boolean";

    /// <summary>
    /// TypeScript symbol type.
    /// See https://www.typescriptlang.org/docs/handbook/symbols.html
    /// </summary>
    public const string Symbol = "symbol";

    /// <summary>
    /// TypeScript undefined type.
    /// </summary>
    public const string Undefined = "undefined";

    /// <summary>
    /// TypeScript object type.
    /// The TypeScript object type represents all values that are not in primitive types.
    /// All other types here are primitive types.
    /// </summary>
    public const string Object = "object";

    /// <summary>
    /// TypeScript function type.
    /// </summary>
    public const string Function = "function";
}